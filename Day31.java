public class Day31 {
    public static void main(String[] args) {
        String ac = "14";
        int ab = Integer.parseInt(ac);
        System.out.println(ab);
        
        String a = "10";
        short b = Short.parseShort(a);
        System.out.println(b);
        
        String may = "2005";
        long maya = Long.parseLong(may);
        System.out.println(maya);
        
        String c = "10";
        float h = Float.parseFloat(c);
        System.out.println(h);
        
        String i ="20";
        double n = Double.parseDouble(i);
        System.out.println(n);
        
        String y = "30";
        byte k = Byte.parseByte(y);
        System.out.println(k);
        
        String m = "false";
        boolean boo = Boolean.parseBoolean(m);
        System.out.println(boo);
        
        String konvert = "maya";
        char z = konvert.charAt(0);
        System.out.println(z);
    }
}


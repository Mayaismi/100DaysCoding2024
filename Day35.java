public class Day35 {
    public static void main(String[] args) {
        //Operator aritmatika: penjumlahan & pengurangan
        
        //Denontasi operator aritmatika penjumlahan dan pengurangan
        int a = 90;
        int b = 10;
        
        //penjumlahan
        int hasilPenjumlahan = a+b;
        System.out.println("Hasil penjumlahan : " + a + "+" + b + "=" + hasilPenjumlahan );
        //pengurangan 
        int hasilPengurangan = a-b;
        System.out.println("Hasil penjumlahan : " + a + "-" + b + "=" + hasilPengurangan);
        
        
    }
    
}

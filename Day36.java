public class Day36 {
        public static void main(String[] args) {
        
            // operator aritmatika: perkalian, pembagian serta modulo
            int a = 14;
            int b = 10;
            
            int Perkalian = a*b;
            System.out.println("Perkalian: " + Perkalian );
            
            int Pembagian = a/b;
            System.out.println("Pembagian: " + Pembagian);
            
            int Modulo = a%b;
            System.out.println("Modulo: "+ Modulo);
        }
         
    
}

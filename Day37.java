public class Day37 {
    public static void main(String[] args) {
        
        //Operator penugasan: pengisian nilai,penambahan dan pengurangan
        int M = 10;
        System.out.println("Hasil pengisian: " + M);
        
        //penambahan
        M +=2;
        System.out.println("Hasil tambah: " +M);
        
        //pengurangan
        M -=5;
        System.out.println("Hasil kali: " + M);
        
        
        
    }
    
}
